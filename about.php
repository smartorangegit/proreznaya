    <script type="text/javascript">

    $(document).ready(function(){
        var slides = document.getElementsByClassName('about-park__slider-item');
        var slidesLength = slides.length;
        var slideIndex = 1;
        var aboutSlider = $('.about-park__slider').slick({
            fade: true,
            nextArrow: '<svg class="controls-right" width="70" height="70"><use xlink:href="#right-arrow-slim"></use></svg>',
            prevArrow: '<svg class="controls-left" width="70" height="70"><use xlink:href="#right-arrow-slim"></use></svg>',
        });
        $("#number-slider").text(1+' / '+slidesLength);
        aboutSlider.on('afterChange', function(event, slick, currentSlide, nextSlide){
            slideIndex = +currentSlide + 1;
            $("#number-slider").text(slideIndex+' / '+slidesLength);
        });

    });
    </script>
